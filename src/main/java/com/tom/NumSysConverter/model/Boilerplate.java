package com.tom.NumSysConverter.model;

public class Boilerplate {
	
	private String helpText = "";
	private String errorText = "Invalid arguments: see help text below for usage.";
	private String conversionText = "Converted values: ";
	private String binText = "Binary value: ";
	private String decText = "Decimal value: ";
	private String hexText = "Hexidecimal value: ";

	//Getters and Setters
	public String getHelpText() {
		return helpText;
	}

	public String getErrorText() {
		return errorText;
	}

	public String getConversionText() {
		return conversionText;
	}

	public String getBinText() {
		return binText;
	}

	public String getDecText() {
		return decText;
	}

	public String getHexText() {
		return hexText;
	}
}
